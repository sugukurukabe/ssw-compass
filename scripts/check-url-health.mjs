/**
 * source-index.jsonl の各 URL の死活を確認し、health report を生成する。
 * Checks each URL in source-index.jsonl and writes a health report.
 * Memeriksa setiap URL di source-index.jsonl dan menulis laporan kesehatan.
 *
 * 使い方 / Usage / Penggunaan:
 *   node scripts/check-url-health.mjs              # report のみ (source-index は変更しない)
 *   node scripts/check-url-health.mjs --write      # source-index の status/lastRunAt も更新
 *   node scripts/check-url-health.mjs --timeout=15000 --concurrency=6
 *
 * 出力 / Output:
 *   data/url-health-report.<YYYY-MM-DD>.md
 *
 * 終了コード / Exit code:
 *   失敗 URL があると 1 (CI で検知可能)。--allow-failures で常に 0。
 */

import { writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { readJsonl } from "./rag-shared.mjs";

const sourceIndexPath = resolve(process.cwd(), "data/source-index.jsonl");

function parseFlags(argv) {
  const flags = {
    write: false,
    allowFailures: false,
    timeoutMs: 15000,
    concurrency: 6,
  };
  for (const raw of argv) {
    if (raw === "--write") flags.write = true;
    else if (raw === "--allow-failures") flags.allowFailures = true;
    else if (raw.startsWith("--timeout=")) flags.timeoutMs = Number(raw.slice("--timeout=".length));
    else if (raw.startsWith("--concurrency="))
      flags.concurrency = Number(raw.slice("--concurrency=".length));
  }
  return flags;
}

/**
 * 1 URL の死活を確認する。HEAD → 失敗時 GET にフォールバック。
 * Probes a single URL: HEAD first, then GET fallback (some go.jp hosts reject HEAD).
 */
async function probe(url, timeoutMs) {
  const attempt = async (method) => {
    const controller = AbortSignal.timeout(timeoutMs);
    const response = await fetch(url, {
      method,
      redirect: "follow",
      signal: controller,
      headers: {
        // 一部の公式サイトは UA なしの自動アクセスを拒否するため明示する。
        "User-Agent": "ssw-compass-url-health/1.0 (+https://mcp.ssw-compass.jp)",
      },
    });
    return response.status;
  };
  try {
    const headStatus = await attempt("HEAD");
    if (headStatus >= 200 && headStatus < 400) {
      return { ok: true, status: headStatus, reason: null };
    }
    // HEAD が 4xx/5xx の場合は GET を試す (405 Method Not Allowed 等)。
    const getStatus = await attempt("GET");
    if (getStatus >= 200 && getStatus < 400) {
      return { ok: true, status: getStatus, reason: null };
    }
    // 403/401 は bot ブロックの可能性が高く、人間がブラウザで開けば有効な場合がある。
    // 404/410 は恒久的に消失している可能性が高い。注記して人手確認を促す。
    const note =
      getStatus === 403 || getStatus === 401
        ? " (bot ブロックの可能性; 手動確認)"
        : getStatus === 404 || getStatus === 410
          ? " (恒久的消失の可能性; 差し替え要)"
          : "";
    return { ok: false, status: getStatus, reason: `HTTP ${getStatus}${note}` };
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    const reason = /timeout|timed out|aborted/i.test(message) ? "timeout" : message;
    return { ok: false, status: null, reason };
  }
}

async function runPool(items, concurrency, worker) {
  const results = new Array(items.length);
  let cursor = 0;
  const runners = Array.from({ length: Math.min(concurrency, items.length) }, async () => {
    while (cursor < items.length) {
      const index = cursor++;
      results[index] = await worker(items[index], index);
    }
  });
  await Promise.all(runners);
  return results;
}

function buildReport(date, entries, probes) {
  const okCount = probes.filter((p) => p.ok).length;
  const failCount = probes.length - okCount;
  const lines = [];
  lines.push(`# URL Health Report — ${date}`);
  lines.push("");
  lines.push("> 自動生成 / Generated by `node scripts/check-url-health.mjs`.");
  lines.push("");
  lines.push("## Summary");
  lines.push("");
  lines.push("| Metric | Value |");
  lines.push("|---|---:|");
  lines.push(`| Total entries | ${probes.length} |`);
  lines.push(`| OK (2xx/3xx) | ${okCount} |`);
  lines.push(`| Failed / timeout | ${failCount} |`);
  lines.push("");
  if (failCount > 0) {
    lines.push("## Failed entries");
    lines.push("");
    lines.push("| ID | URL | Reason |");
    lines.push("|---|---|---|");
    for (const [index, probe] of probes.entries()) {
      if (probe.ok) continue;
      const entry = entries[index];
      lines.push(`| \`${entry.id}\` | \`${entry.url}\` | ${probe.reason ?? "unknown"} |`);
    }
    lines.push("");
  }
  lines.push("## Next action");
  lines.push("");
  lines.push(
    '失敗エントリは一次ソースを再確認し、新 URL へ差し替えるか `status: "withdrawn"` にする。',
  );
  lines.push("手順: `docs/law-updates-maintenance-runbook.md` と `docs/rag-pipeline-runbook.md`。");
  lines.push("");
  return `${lines.join("\n")}\n`;
}

async function main() {
  const flags = parseFlags(process.argv.slice(2));
  const date = new Date().toISOString().slice(0, 10);
  const entries = await readJsonl(sourceIndexPath);

  console.log(
    `Probing ${entries.length} URLs (concurrency=${flags.concurrency}, timeout=${flags.timeoutMs}ms)...`,
  );
  const probes = await runPool(entries, flags.concurrency, async (entry) => {
    if (typeof entry.url !== "string") return { ok: false, status: null, reason: "missing url" };
    const result = await probe(entry.url, flags.timeoutMs);
    const mark = result.ok ? "OK " : "FAIL";
    console.log(`  ${mark} ${entry.id} ${result.status ?? ""} ${result.reason ?? ""}`.trimEnd());
    return result;
  });

  const okCount = probes.filter((p) => p.ok).length;
  const failCount = probes.length - okCount;

  const reportPath = resolve(process.cwd(), `data/url-health-report.${date}.md`);
  await writeFile(reportPath, buildReport(date, entries, probes), "utf-8");
  console.log(`\nReport written: data/url-health-report.${date}.md`);
  console.log(`OK: ${okCount}  Failed: ${failCount}`);

  if (flags.write) {
    const updated = entries.map((entry, index) => {
      const probe = probes[index];
      return {
        ...entry,
        status: probe.ok ? "ok" : "failed",
        lastRunAt: date,
        ...(probe.ok ? {} : { lastFailureReason: probe.reason ?? "unknown" }),
      };
    });
    const jsonl = `${updated.map((e) => JSON.stringify(e)).join("\n")}\n`;
    await writeFile(sourceIndexPath, jsonl, "utf-8");
    console.log("source-index.jsonl status fields updated (--write).");
  }

  if (failCount > 0 && !flags.allowFailures) {
    process.exitCode = 1;
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.stack : String(error));
  process.exit(1);
});
